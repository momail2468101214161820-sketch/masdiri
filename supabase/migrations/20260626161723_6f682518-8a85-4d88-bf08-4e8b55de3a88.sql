-- 1) Ensure the new Education category exists
SELECT public.ensure_category('education', 'تعليم');

-- 2) Replace inference function with broader, more accurate keyword rules
CREATE OR REPLACE FUNCTION public.infer_article_category(_title text, _content text)
RETURNS uuid
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
DECLARE
  txt text := lower(COALESCE(_title,'') || ' ' || COALESCE(_content,''));
  slug text;
  nm text;
BEGIN
  -- Accidents / Crime / Disasters (highest priority — life/safety)
  IF txt ~ 'حادث|حوادث|تصادم|اصطدام|انقلاب سيارة|جريمة|جرائم|قتيل|قتلى|مقتل|اغتيال|ضحايا|ضحية|زلزال|حريق|حرائق|انفجار|نيابة|محكمة|قاضي|قضاء|ضبط|بلاغ|سرقة|سرقات|اختطاف|خطف|اعتداء|ذبح|طعن|طلق ناري|إطلاق نار|غرق|سقوط|انهيار|تسمم' THEN
    slug := 'accidents'; nm := 'حوادث';

  -- Sports
  ELSIF txt ~ 'الأهلي|الزمالك|بيراميدز|الاتحاد|الإسماعيلي|المصري|سيراميكا|إنبي|كرة القدم|كرة السلة|كرة اليد|كرة الطائرة|مباراة|مباريات|الدوري|الكأس|كأس|لاعب|لاعبين|اللاعب|منتخب|بطولة|البطولة|مدرب|تدريب|هدف|أهداف|الملعب|ملعب|الكرة|أولمبياد|أولمبي|الفيفا|فيفا|ميسي|رونالدو|صلاح|كهربا|الشناوي|تريزيجيه|حسام حسن|كولر|اللجنة الأولمبية|الاتحاد المصري|سباق|جري|سباحة|ملاكمة|تنس|اسكواش' THEN
    slug := 'sports'; nm := 'رياضة';

  -- Prices (consumer prices / commodities)
  ELSIF txt ~ 'سعر|أسعار|تسعيرة|ارتفاع أسعار|انخفاض أسعار|الذهب|عيار|الفضة|الفراخ|الدواجن|اللحوم|الخضروات|الفاكهة|البنزين|السولار|الوقود|الغاز المنزلي|الأسمنت|الحديد|الأرز|السكر|الزيت' THEN
    slug := 'prices'; nm := 'أسعار';

  -- Education
  ELSIF txt ~ 'وزارة التربية|وزير التعليم|التعليم|تعليمي|مدرسة|مدارس|طالب|طلاب|طلبة|امتحان|امتحانات|نتيجة|نتائج|الثانوية|الإعدادية|الابتدائية|الجامعة|جامعات|كلية|كليات|تنسيق|الأزهر|الباكالوريا|الدبلوم' THEN
    slug := 'education'; nm := 'تعليم';

  -- Health
  ELSIF txt ~ 'صحة|صحي|صحية|طبيب|أطباء|مستشفى|مستشفيات|دواء|أدوية|علاج|وباء|أوبئة|فيروس|فيروسات|لقاح|تطعيم|مرض|أمراض|إصابة|إصابات|جراحة|عملية جراحية|وزارة الصحة|تأمين صحي|سرطان|قلب|سكر|ضغط|كورونا|كوفيد' THEN
    slug := 'health'; nm := 'صحة وأسرة';

  -- Technology
  ELSIF txt ~ 'تكنولوجيا|تقنية|ذكاء اصطناعي|chatgpt|ai|تطبيق|تطبيقات|إنترنت|واي فاي|هاتف|هواتف|موبايل|آيفون|iphone|سامسونج|samsung|جوجل|google|فيسبوك|facebook|واتساب|whatsapp|تيك توك|tiktok|انستجرام|instagram|تويتر|twitter|x.com|روبوت|سوفتوير|برمجة|مطور|تحديث|نظام تشغيل|أندرويد|android|ios|لاب توب|كمبيوتر|بطارية|شريحة|معالج|chip|جي بي تي|ميتا|meta|بلاي ستيشن|اكس بوكس|ألعاب إلكترونية|متاهيرس|ميتافيرس' THEN
    slug := 'technology'; nm := 'تكنولوجيا';

  -- Economy / Finance
  ELSIF txt ~ 'اقتصاد|اقتصادي|اقتصادية|بنك|بنوك|المركزي|استثمار|استثمارات|تمويل|صادرات|واردات|تضخم|ميزانية|موازنة|بترول|نفط|غاز|تجارة|بورصة|دولار|يورو|جنيه|عملة|عملات|قرض|قروض|صندوق النقد|ضرائب|ضريبة|جمارك|مديونية|دين|ديون' THEN
    slug := 'economy'; nm := 'اقتصاد';

  -- Entertainment / Arts / Celebrities
  ELSIF txt ~ 'فنان|فنانة|فنانين|مسلسل|مسلسلات|سينما|مهرجان|مهرجانات|أغنية|أغاني|ألبوم|مطرب|مطربة|ممثل|ممثلة|فيلم|أفلام|مسرح|مسرحية|دراما|كليب|أوبرا|موسيقى|نجمة|نجم|هوليوود|بوليوود|تامر حسني|عمرو دياب|محمد رمضان|أنغام|إليسا|نانسي|روبي|ياسمين|مهرجان الجونة|مهرجان القاهرة|أزياء|موضة' THEN
    slug := 'entertainment'; nm := 'فن ومنوعات';

  -- Politics (default for governmental / international)
  ELSIF txt ~ 'سياسة|سياسي|سياسية|الرئيس|السيسي|الحكومة|مجلس الوزراء|مجلس النواب|البرلمان|وزير|وزارة|سفير|سفارة|دبلوماس|قمة|معاهدة|اتفاقية|الأمم المتحدة|الجامعة العربية|الاتحاد الأوروبي|أمريكا|الولايات المتحدة|روسيا|الصين|إيران|إسرائيل|غزة|فلسطين|سوريا|لبنان|ليبيا|السودان|اليمن|تركيا|أردوغان|بوتين|ترامب|بايدن|نتنياهو|حماس|حزب الله' THEN
    slug := 'politics'; nm := 'سياسة';

  ELSE
    slug := 'politics'; nm := 'سياسة';
  END IF;

  RETURN public.ensure_category(slug, nm);
END;
$function$;

-- 3) Re-classify every article using the new function (force, ignoring previous category)
UPDATE public.articles
SET category_id = public.infer_article_category(title, content);